    public void mostrarHosteleros() {

        ModeloTablaHosteleros m;
        m = (ModeloTablaHosteleros) TablaHosteleros.getModel();
        m.setFilas(pr.getHostelerosDAO().getHosteleros());
        if (m.getRowCount() > 0) {
            TablaHosteleros.setRowSelectionInterval(0, 0); 
            
        }
    }

